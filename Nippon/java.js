const kontaktKnap = document.getElementById("kontaktKnap")

kontaktKnap.addEventListener('mouseover', () => {
  kontaktKnap.style.backgroundColor = '#98AF77';
});

kontaktKnap.addEventListener('mouseout', () => {
  kontaktKnap.style.backgroundColor = '';
});



